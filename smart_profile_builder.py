# Smart Profile Builder for Internship Applications
#Pseudocode
#start
# ask user for input ie fullname, age, email and interest
# ensure the user enters numbers only
# remove white spaces from the input, make email lowercase, capitalize the first letter of each name, split the interests into a list and remove whitespaces
# remove duplicates and sort the list alphabetically
# determine age group using the parameters given
# Display name in reversed and uppercased using string methods
# display age groups
# show number of interests
# Show the first and last interests
# Sort the interests and display
# add eligibility for the internship
