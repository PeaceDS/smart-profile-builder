while True:
    full_name = input("Enter your full name: ")
    age_input = input("Enter your age: ")
    email = input("Enter your email address: ")
    interests_input = input("Enter 3 interests (comma-separated): ")

    if not age_input.isdigit():
        print("Invalid age. Please enter numbers only.")
        continue
    age = int(age_input)

    full_name = full_name.strip().title()
    email = email.strip().lower()

    interests = interests_input.split(",")
    cleaned_interests = []

    for item in interests:
        item = item.strip().title()
        if item not in cleaned_interests:
            cleaned_interests.append(item)

    cleaned_interests.sort()

    if age >= 13 and age <= 17:
        age_group = "Teen"
    elif age >= 18 and age <= 25:
        age_group = "Young Adult"
    else:
        age_group = "Other"

    print("Profile Summary")
    reversed_name = full_name[::-1].upper()
    print(f"Name (reversed & uppercase): {reversed_name}")

    print(f"Age Group: {age_group}")
    print("Number of Interests: {}".format(len(cleaned_interests)))

    if len(cleaned_interests) > 0:
        print("First Interest: {}".format(cleaned_interests[0]))
        print("Last Interest: {}".format(cleaned_interests[-1]))

    print("Sorted Interests: {}".format(", ".join(cleaned_interests)))

    if age >= 18:
        print("You are eligible for internship.")
    else:
        print("You are not eligible for internship yet.")

    again = input("\nDo you want to enter another profile? (yes/no): ").strip().lower()
    if again != "yes":
        print("Thank you for using the Smart Profile Builder.")
        break